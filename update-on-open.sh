#!/bin/bash
# Скрипт для обновления данных при открытии панели
# Запускается при каждом открытии панели

echo "🔄 Обновление данных Alfred Dashboard при открытии..."

# Переходим в директорию скриптов
cd "$(dirname "$0")"

# Запускаем скрипт обновления
node scripts/update-on-open.js

# Коммитим и пушим изменения если есть git
if [ -d ".git" ]; then
    git add data.json
    git commit -m "🤖 Auto-update: данные обновлены при открытии панели $(date '+%Y-%m-%d %H:%M:%S')" > /dev/null 2>&1
    git push > /dev/null 2>&1
    echo "✅ Данные обновлены и отправлены в репозиторий"
else
    echo "✅ Данные обновлены локально"
fi

echo "🎯 Режим: обновление по запросу (экономия токенов 100%)"